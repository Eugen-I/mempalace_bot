def classify_wing(query: str) -> str | None:
    """
    Определяет наиболее подходящее крыло по запросу.
    Возвращает название крыла или None, если тема не определена.
    """
    if not query or len(query.strip()) < 3:
        return None
    
    query_lower = query.lower()
    
    scores = {}
    for wing, keywords in WING_KEYWORDS.items():
        score = sum(1 for kw in keywords if kw in query_lower)
        if score > 0:
            scores[wing] = score
    
    if not scores:
        logger.debug(f"[WING_CLASSIFIER] Тема не определена для: '{query[:50]}...'")
        return None
    
    # Выбираем крыло с максимальным количеством совпадений
    best_wing = max(scores, key=scores.get)
    logger.info(f"[WING_CLASSIFIER] ✅ Определено крыло: '{best_wing}' (score={scores[best_wing]}) для: '{query[:50]}...'")
    return best_wing