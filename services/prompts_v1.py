def get_summary_prompt(dialog_slice: str, context_prefix: str = "") -> str:
    """Промпт для генерации краткого саммари диалога."""
    return (
        "Ты — эксперт по суммаризации диалогов для системы MemPalace.\n"
        "ЗАДАЧА: Создать КРАТКОЕ, связное резюме предоставленного фрагмента диалога.\n"
        "ОГРАНИЧЕНИЯ:\n"
        "1. МАКСИМУМ 3-7 ПРЕДЛОЖЕНИЙ ИЛИ 45-50 СЛОВ.\n"
        "2. Только ключевые выводы, ключевые решения и ключевые инсайты.\n"
        "3. НЕ ПОВТОРЯЙ последнее сообщение дословно. ОБОБЩИ до главной сути.\n"
        "4. Сохрани контекст предыдущих обсуждений, если они важны.\n"
        "5. Будь максимально краток.\n"
        f"{context_prefix}{dialog_slice}\n"
        "РЕЗЮМЕ:"
    )