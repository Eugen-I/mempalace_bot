def get_smart_prompt(context: str = "", query: str = "", has_images: bool = False) -> str:
    """
    Динамически собирает системный промпт на основе конфигурации PROMPT_MIXING_RULES.
    """
    prompt_parts = []
    
    # 1. Вступление
    prompt_parts.append("Ты — аналитический ИИ-помощник MemPalace.\n")

    # 2. Контекст из базы знаний (если есть)
    if context.strip():
        prompt_parts.append(f"Контекст из личных заметок MemPalace:\n{context}\nЭти данные имеют ПРИОРИТЕТ над общими знаниями.\n")

    # 3. Выбор активного фокуса по правилам
    active_rule = None
    for rule in PROMPT_MIXING_RULES:
        if rule["condition"](query, has_images):
            active_rule = rule
            break
    
    if active_rule:
        # Добавляем части фокуса
        for part in active_rule["parts"]:
            prompt_parts.append(part)
        
        # Добавляем связующий текст (glue), если он есть
        if active_rule.get("glue"):
            prompt_parts.append(active_rule["glue"])
            
        # Логируем выбор (опционально, можно убрать)
    #print(f"[PROMPT] Selected rule: {active_rule['name']}")
    
    # 4. Базовые правила форматирования (всегда в конце)
    prompt_parts.append(BASE_RULES)
    if active_rule:
        logger.info(f"[PROMPT_ENGINE] Selected rule: {active_rule['name']}")

    return "\n".join(prompt_parts)

